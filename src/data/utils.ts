export function getQuizResponse(isCorrect: boolean): string {
  if (isCorrect) {
    return correctResponses[Math.floor(Math.random() * correctResponses.length)]
  } else {
    return wrongResponses[Math.floor(Math.random() * wrongResponses.length)]
  }
}

